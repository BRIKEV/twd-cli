import fs from 'node:fs';
import path from 'node:path';
import puppeteer from 'puppeteer';

const URL = 'http://localhost:5173';
const NYC_DIR = path.resolve(process.cwd(), '.nyc_output');
const PUPPETEER_ARGS = [
  '--no-sandbox',
  '--disable-setuid-sandbox',
  // Prevent Chromium from de-prioritizing renderers it thinks are hidden —
  // relevant when we run multiple contexts concurrently in headless mode.
  '--disable-background-timer-throttling',
  '--disable-renderer-backgrounding',
  '--disable-backgrounding-occluded-windows',
];
const TIMEOUT = 10000;

// Test IDs in twd-js are generated by Math.random() at module load
// (twd/src/runner.ts: `const generateId = () => Math.random()...`), so they
// are NOT stable across browser contexts. Each worker therefore enumerates
// its own context's __TWD_STATE__.handlers and self-selects the slots where
// idx % N === workerIndex. Registration ORDER is stable (source-code order
// of describe/it calls), so the partition is deterministic and disjoint.
async function runWorker(workerIndex, N, page) {
  const result = await page.evaluate(async (workerIndex, N) => {
    const allIds = Array.from(window.__TWD_STATE__.handlers.values())
      .filter((h) => h.type === 'test')
      .map((h) => h.id);
    const myIds = allIds.filter((_, idx) => idx % N === workerIndex);

    const TestRunner = window.__testRunner;
    const status = [];
    const runner = new TestRunner({
      onStart: () => {},
      onPass: (t) => status.push({ id: t.id, status: 'pass' }),
      onFail: (t, err) => status.push({
        id: t.id,
        status: 'fail',
        error: `${err.message} (at ${window.location.href})`,
      }),
      onSkip: (t) => status.push({ id: t.id, status: 'skip' }),
    });
    await runner.runByIds(myIds);
    return { status, totalInContext: allIds.length, selected: myIds.length };
  }, workerIndex, N);

  console.log(
    `Worker ${workerIndex}: ${result.selected}/${result.totalInContext} tests selected`
  );

  const coverage = await page.evaluate(() => window.__coverage__);
  if (coverage) {
    const outPath = path.join(NYC_DIR, `out-${workerIndex}.json`);
    fs.writeFileSync(outPath, JSON.stringify(coverage));
    console.log(`Worker ${workerIndex}: coverage → ${outPath}`);
  } else {
    console.log(`Worker ${workerIndex}: no __coverage__ on window`);
  }

  return result.status;
}

async function main() {
  const N = parseInt(process.argv[2], 10) || 2;
  console.log(`Parallel POC — N=${N}, URL=${URL}`);

  fs.rmSync(NYC_DIR, { recursive: true, force: true });
  fs.mkdirSync(NYC_DIR, { recursive: true });

  const browser = await puppeteer.launch({
    headless: true,
    args: PUPPETEER_ARGS,
  });

  try {
    console.time('Parallel test time');

    const workerPromises = [];
    for (let i = 0; i < N; i++) {
      const workerIndex = i;
      workerPromises.push((async () => {
        const ctx = await browser.createBrowserContext();
        const page = await ctx.newPage();
        await page.goto(URL);
        await page.waitForSelector('#twd-sidebar-root', { timeout: TIMEOUT });
        const result = await runWorker(workerIndex, N, page);
        await ctx.close();
        return result;
      })());
    }

    const results = await Promise.all(workerPromises);
    console.timeEnd('Parallel test time');

    results.forEach((status, i) => {
      const failures = status.filter((s) => s.status === 'fail');
      if (failures.length > 0) {
        console.log(`\nWorker ${i} failures:`);
        failures.forEach((f) => console.log(`  [${f.id}] ${f.error}`));
      }
    });

    let totalPass = 0;
    let totalFail = 0;
    let totalSkip = 0;
    results.forEach((status, i) => {
      const pass = status.filter((s) => s.status === 'pass').length;
      const fail = status.filter((s) => s.status === 'fail').length;
      const skip = status.filter((s) => s.status === 'skip').length;
      totalPass += pass;
      totalFail += fail;
      totalSkip += skip;
      console.log(`Worker ${i} done: ${pass} passed, ${fail} failed, ${skip} skipped`);
    });
    const totalReported = totalPass + totalFail + totalSkip;
    console.log(
      `Total: ${totalPass} passed, ${totalFail} failed, ${totalSkip} skipped (${totalReported} reported)`
    );

    if (totalFail > 0) {
      process.exitCode = 1;
    }
  } finally {
    await browser.close();
  }
}

main().catch((err) => {
  console.error('POC error:', err);
  process.exit(1);
});
